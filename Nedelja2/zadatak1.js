//За два броја(а и б) исписати њихов: Збир, разлику, производ, количник, остатак при дељењу, степен(а ^ b)

let a = 3;
let b = 4;
switch (0) {
  case 0:
    console.log(a + b);

  case 1:
    console.log(a - b);

  case 2:
    console.log(a * b);

  case 3:
    console.log(a / b);

  case 4:
    console.log(a % b);

  case 5:
    console.log(a ** b);
    break;
}
